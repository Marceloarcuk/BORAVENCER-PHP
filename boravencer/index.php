                        <p>6.2. A classificação final será obtida após os critérios de desempate.</p>
                        <p>6.3. A SECRIA divulgará a lista dos jovens classificados no site da secretaria (http://www.crianca.df.gov.br).</p>

                        <p><b> 7. Dos critérios de desempate</b></p>
                        <p> 7.1 Em caso de empate no argumento final da classificação, terá prioridade o PARTICIPANTE que, na seguinte ordem:</p>
                        <p> a. Obtiver a maior média aritmética entre as disciplinas de Língua Portuguesa e Matemática no boletim de desempenho escolar.</p>
                        <p> b. Residir na região que possui menor Índice de Desenvolvimento Humano.</p>
                        <p> c. Estiver vinculado ao sistema socioeducativo.</p>
                        <p> d. Persistindo o empate, terá prioridade o PARTICIPANTE mais velho.</p>

                        <p><b>8. Da convocação/matrícula</b></p>
                        <p>8.1 O PARTICIPANTE classificado será informado do local onde deverá realizar sua matrícula por meio de correspondência via endereço eletrônico (e-mail) e/ou número de celular cadastrados.</p>
                        <p> 8.2 No ato da matrícula, o PARTICIPANTE deverá apresentar:</p>
                        <p> a. CONCLUINTE (EJA e Ensino Médio): o comprovante de inscrição no projeto #BoraVencer Intensivão; o comprovante de inscrição no Exame Nacional do Ensino Médio (Enem) 2016; cópias: CPF; RG; título de eleitor; comprovante de residência; certificado ou histórico escolar de conclusão do ensino médio na rede pública de ensino urbanas, rurais, regulares ou supletivas, de escolas confessionais, comunitárias e/ou filantrópicas executoras de parceria com o poder público, ambas sem fins lucrativos ou na forma da lei, ou na rede privada em condição de bolsista integral; e uma foto 3x4 recente.</p>
                        <p> b. CURSANDO (EJA e Ensino Médio): o comprovante de inscrição no projeto #BoraVencer Intensivão; o comprovante de inscrição no Exame Nacional do Ensino Médio (Enem) 2016; cópias: CPF; RG; título de eleitor; comprovante de residência; boletim de desempenho escolar do 1º semestre de 2016; declaração de regularmente matriculado no ensino médio ou EJA; declaração escolar para comprovação de que estudou o 1º e 2º ano do Ensino Médio na rede pública de ensino urbanas, rurais, regulares ou supletivas, de escolas confessionais, comunitárias e/ou filantrópicas executoras de parceria com o poder público, ambas sem fins lucrativos ou na forma da lei, ou declaração de que estudou o 1º e 2º ano na rede privada em condição de bolsista integral; e uma foto 3x4 recente.</p>
                        <p>8.3 O PARTICIPANTE que for beneficiário de programas sociais deve apresentar comprovante de inscrição no Cadastro Único para Programas Sociais (CadÚnico).</p>
                        <p>8.4 Caso o PARTICIPANTE convocado não compareça até o 2º (segundo) dia de aula, será substituído por outro classificado constante da relação.</p>
                        <p>8.5 Em caso de desistência, o PARTICIPANTE não poderá se candidatar no Programa nas próximas duas edições.</p>
                        <p><b>9. Das disposições finais</b></p>
                        <p>9.1. A inscrição do PARTICIPANTE implica a aceitação expressa das disposições, das diretrizes e dos procedimentos contidos nesta Chamada Pública.</p>
                        <p>9.2. Acarretará a eliminação do PARTICIPANTE, sem prejuízo das sanções penais cabíveis, a burla ou a tentativa de burla na apresentação de qualquer documento comprobatório ou prestar informação falsa ou inexata no sistema de inscrição ou descumprimento de quaisquer das normas estipuladas nesta Chamada Pública.</p>
                        <p>9.3. Os PARTICIPANTES assumirão total responsabilidade pelas informações prestadas, arcando com as consequências de eventuais erros no preenchimento do Formulário de Inscrição, podendo ser excluídos do Processo Seletivo Simplificado, caso a inscrição não esteja de acordo com o estabelecido nesta Chamada Pública.</p>
                        <p>9.4. É de inteira responsabilidade do PARTICIPANTE acompanhar a publicação de comunicados referentes a este processo seletivo simplificado, no endereço eletrônico <a href="http://www.crianca.df.gov.br/">http://www.crianca.df.gov.br</a>.</p>
                        <p>9.5. Não será fornecido ao PARTICIPANTE documento comprobatório de classificação ou aprovação no presente processo seletivo, valendo, para esse fim, a publicação da lista dos selecionados no site da Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude <a href="http://www.crianca.df.gov.br/">(http://www.crianca.df.gov.br)</a>. </p>
                        <p>9.6 Todos os horários previstos nesta Chamada Pública correspondem ao horário oficial de Brasília.</p>
                        <p>9.7 Os casos omissos e eventuais dúvidas referentes a este Edital serão resolvidos e esclarecidos pela Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude.</p>





