                        <h5><b>1. Das disposições preliminares</b></h5>
                        <p>1.1 As vagas serão distribuídas conforme demanda e planejamento pedagógico nas regiões administrativas do Distrito Federal pelos turnos matutino, vespertino e noturno. Dessas vagas, 5% são destinadas a jovens com deficiência.<p>
                        <p>1.2 As inscrições serão realizadas das 14h do dia 15 de agosto de 2016, às 23h59min do dia 19 de agosto de 2016, horários oficiais de Brasília-DF.</p>
                        <p>1.3 Podem se inscrever no programa, jovens entre 15 e 29 anos, que atendam a uma das condições a seguir:
                            Matriculados no 3º ano do ensino médio da rede pública do DF, urbanas, rurais, regulares ou supletivas, de escolas confessionais, comunitárias e/ou filantrópicas executoras de parceria com o poder público, ambas sem fins lucrativos ou na forma da lei, e bolsistas integrais em escolas particulares.</p>
                        <p> - Devem estar inscritos, obrigatoriamente, no Enem 2016 e não podem estar participando de projetos sociais de acesso ao ensino superior.</p>
                        <p>b) Matriculados na Educação de Jovens e Adultos (EJA) equivalente ao 3º ano do ensino médio.</p>
                        <p> - Devem estar inscritos, obrigatoriamente, no Enem 2016 e não podem estar participando de projetos sociais de acesso ao ensino superior.</p>
                        <p>c) Egressos do 3º ano do ensino médio das escolas da rede pública do DF, urbanas, rurais, regulares ou supletivas, de escolas confessionais, comunitárias e/ou filantrópicas executoras de parceria com o poder público, ambas sem fins lucrativos ou na forma da lei, e bolsistas integrais em escolas particulares.</p>
                        <p> - Devem estar inscritos, obrigatoriamente, no Enem 2016 e não podem estudar em universidades públicas ou participar de projetos sociais de acesso ao ensino superior.</p>
                        <p>d) Egressos da Educação de Jovens e Adultos (EJA) em nível de ensino médio.</p>
                        <p> -  Devem estar inscritos, obrigatoriamente, no Enem 2016 e não podem estudar em universidades públicas ou participar de projetos sociais de acesso ao ensino superior.</p>
                        <p>1.4 <b> Prioritariamente</b>, o PARTICIPANTE estudará na macrorregião em que se encontra a região administrativa em que reside. (Quadro 1).
                            O Participante que estiver concorrendo a macrorregião de:</p>
                        <p> a) <b>Taguatinga</b> - poderá estudar nas regiões administrativas de Ceilândia, Samambaia ou Taguatinga.</p>
                        <p> b) <b>Gama</b> – estudará na região administrativa do Gama. </p>
                        <p> c) <b>Sobradinho</b> – poderá estudar nas regiões administrativas de Planaltina e Sobradinho I. </p>
                        <p> d) <b>Brasília</b> - poderá estudar nas regiões administrativas do Guará ou Plano Piloto.</p>
                        <p>1. 6 <b>Excepcionalmente</b>, no caso de existirem disponibilidade de vagas em outras macrorregiões, a Secretaria poderá realizar remanejamento, conforme a segunda opção informada pelo PARTICIPANTE, no ato da inscrição.</p><br>

