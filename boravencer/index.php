                        <h5><b>2. Das inscrições</b></h5>

                        <p>2.1. Antes de efetuar sua inscrição, o PARTICIPANTE deverá ler esta Chamada Pública para certificar-se de que preenche todos os requisitos exigidos para a participação no Programa #BoraVencer Intensivão e aceita todas as condições nela estabelecidas.</p>
                        <p>2.2. A inscrição do PARTICIPANTE implicará ciência e aceitação das condições estabelecidas no inteiro teor desta Chamada Pública, das quais, não poderá alegar desconhecimento.</p>
                        <p>2.3. A inscrição será realizada, exclusivamente, via Internet, no endereço eletrônico http://crianca.df.gov.br, a partir das 14h do dia 15 de agosto de 2016 até as 23h59min do dia 19 de agosto de 2016, horários oficiais de Brasília-DF.</p>
                        <p>2.4. O PARTICIPANTE que prestar qualquer informação falsa ou inexata, ao se inscrever no Programa, ou que não satisfizer todas as condições estabelecidas nesta Chamada Pública, terá cancelada sua inscrição.</p>
                        <p>2.5. A Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude não se responsabiliza por solicitação de inscrição não recebida devido a quaisquer motivos de ordem técnica dos computadores, falhas de comunicação, congestionamento das linhas de comunicação, procedimento indevido do PARTICIPANTE, bem como por outros fatores que impossibilitem a transferência de dados, sendo de responsabilidade exclusiva do PARTICIPANTE acompanhar a situação de sua inscrição.</p>
                        <p>2.6. Em nenhuma hipótese será permitida a inscrição condicional ou fora do prazo.</p>
                        O PARTICIPANTE deve ter em mãos, no ato da inscrição, o seu número de Cadastro de Pessoa Física (CPF), o seu número do documento de identidade, para aqueles beneficiários de programas sociais, o número do Cadastro Único para Programas Sociais (CadÚnico), o número de inscrição do Enem 2016 e documentos obrigatórios para a efetivação da inscrição.

                        <h5><b>3. Do preenchimento das inscrições</b></h5>
                        <p> 3.1. O PARTICIPANTE deverá, no ato da inscrição, informar obrigatoriamente um endereço de e-mail válido.</p>
                        <p> 3.2. O endereço de e-mail informado na inscrição pelo PARTICIPANTE é único, não sendo permitida a utilização de um mesmo endereço de e-mail por outro PARTICIPANTE.</p>
                        <p> 3.3. A Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude utilizará o e-mail e/ou número de celular cadastrados para enviar aos PARTICIPANTES informações relativas ao Processo Seletivo.</p>
                        <p> 3.4. A Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude não se responsabiliza pelo envio de informações a terceiros decorrente de cadastramento indevido de endereço de e-mail e/ou celular pelo PARTICIPANTE.</p>
                        <p> 3.5. O PARTICIPANTE inscrito no Cadastro Único para Programas Sociais no Governo Federal (CadÚnico) deve informar o número do seu cadastro.</p>
                        <p> 3.6. No momento da inscrição, o participante terá o direito de escolher até duas opções de turno em que deseja estudar. Uma vez efetivada a inscrição, não será permitida, em hipótese alguma, a sua alteração.</p>
                        <p> 3.7. No preenchimento da inscrição o PARTICIPANTE deve informar, obrigatoriamente, o número de inscrição do Enem 2016; as notas do boletim de desempenho do 3º ano do Ensino Médio, referentes às disciplinas de Língua Portuguesa e Matemática (egressos); notas do boletim de desempenho das disciplinas de Língua Portuguesa e Matemática, referentes ao 1º semestre do 3º ano de 2016 (cursandos).</p>
                        <p> 3.8. O PARTICIPANTE deverá responsabilizar-se pelo preenchimento correto e fidedigno da ficha de inscrição.</p>
                        <p> 3.9. O número de inscrição e a senha deverão ser mantidos sob a guarda do PARTICIPANTE e são indispensáveis para o acompanhamento do processo de inscrição, para a consulta e para a impressão do CARTÃO DE CONFIRMAÇÃO DA INSCRIÇÃO.</p>
                        <p> 3.10. O login do PARTICIPANTE será o seu endereço de e-mail, e a senha de acesso ao sistema é pessoal, intransferível e de inteira responsabilidade do PARTICIPANTE.</p>
                        <p>3.11 É de responsabilidade do PARTICIPANTE verificar se a inscrição foi concluída com sucesso.</p>
                        <p>3.12 As alterações nos dados cadastrais são permitidas apenas durante o período de inscrição estabelecido nesta Chamada Pública.</p>

                        <p><b>4. Da confirmação da inscrição</b></p>
                        <p> 4.1. A inscrição será confirmada após o preenchimento da ficha de inscrição no endereço eletrônico: <a href="http://www.crianca.df.gov.br">http://crianca.df.gov.br</a> </p>
                        <p>4.2.  O CARTÃO DE CONFIRMAÇÃO DA INSCRIÇÃO contém: nome completo; número de inscrição; CPF; data e hora.</p>
                        <p><b>5. Do Programa</b></p>
                        <p>5.1. As aulas deverão ser ministradas nas áreas de conhecimento a seguir:</p>
