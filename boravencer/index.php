                        <p>5.2. A carga horária total dos cursos será de 200 horas/aula na modalidade presencial e, se necessário, a distância.</p>
                        <p>5.3. A carga horária diária dos cursos será de 4 ou 5 horas, incluído intervalo de 20 minutos para descanso.</p>
                        <p>5.4. Os cursos serão ministrados nos turnos matutino, vespertino e noturno.</p>
                        <p>5.5.  As aulas serão realizadas em dias úteis, de segunda a sexta-feira, com quatro horas diárias e vinte horas semanais. Poderão ocorrer aulas aos sábados e/ou feriados, caso não haja tempo hábil para o cumprimento da carga horária total dos cursos, dentro do calendário de execução definido ou em situações específicas.</p>
                        <p>5.6. Será exigida frequência mínima de 75% da carga horária total dos cursos.</p>

                        <p><b>6. Da seleção/classificação</b></p>
                        <p>6.1. A classificação para a participação do Programa se dará por meio das somas das seguintes pontuações:</p>

