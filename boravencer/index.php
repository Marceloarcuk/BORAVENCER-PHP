                        <p>A Secretaria de Estado de Políticas para Crianças, Adolescentes e Juventude torna público o processo seletivo simplificado para selecionar jovens para o preenchimento de 1.750 (um mil) vagas para o projeto #BoraVencer Intensivão, que ofertará curso preparatório intensivo gratuito para o Exame Nacional do Ensino Médio (ENEM), em 4 (quatro) macrorregiões do Distrito Federal, conforme Quadro de Vagas (Quadro 1) desta Chamada.</p>


